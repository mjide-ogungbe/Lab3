#!/usr/bin/env python3
def write_name_to_file():
    # Get the user's name
    user_name = input("Enter your name: ")

    # Open a text file named "output.txt" in write mode
    with open("output.txt", "w") as file:
        # Write the user's name to the file
        file.write("Your name: " + user_name)

    print("Name written to output.txt successfully.")

# Call the function to write the name to the file
write_name_to_file()

